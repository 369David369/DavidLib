package io.github.splotycode.mosaik.util.commoni;

/**
 * General Interface to represent a Object that has a name.
 * This name might be human readable but it doesn't need to be
 */
public interface Nameable {

    /**
     * The name of this Object
     * This name might be human readable but it doesn't need to be
     */
    String name();

}
