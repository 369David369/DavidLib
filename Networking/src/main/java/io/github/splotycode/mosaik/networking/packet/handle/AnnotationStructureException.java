package io.github.splotycode.mosaik.networking.packet.handle;

public class AnnotationStructureException extends RuntimeException {

    public AnnotationStructureException() {
    }

    public AnnotationStructureException(String s) {
        super(s);
    }

    public AnnotationStructureException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public AnnotationStructureException(Throwable throwable) {
        super(throwable);
    }

    public AnnotationStructureException(String s, Throwable throwable, boolean b, boolean b1) {
        super(s, throwable, b, b1);
    }
}
